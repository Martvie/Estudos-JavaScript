var agora = new Date
var diaSema = agora.getDay()
switch(diaSema){
    case 0:
        console.log('Domingo')
        break
    case 1:
        console.log('Segunda')
        break
    case 2:
        console.log('Terçaa')
        break
    case 3:
        console.log('Quartaa')
        break
    case 4:
        console.log('Quinta')
        break
    case 5:
        console.log('Sexta')
        break
    case 6:
        console.log('Sábado')
        break
}